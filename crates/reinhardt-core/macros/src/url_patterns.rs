use proc_macro2::TokenStream;
use quote::quote;
use syn::{ItemFn, parse2};

/// Flatten a function body into a flat token stream for pattern scanning.
///
/// Recursively descends into `Group` tokens (braces, parentheses, brackets)
/// so that `.endpoint()`, `.viewset()`, and `.mount()` calls inside control
/// flow blocks (`if`, `match`, `for`, etc.) are detected.
fn flatten_body(func: &ItemFn) -> Vec<proc_macro2::TokenTree> {
	fn recurse(
		tokens: impl IntoIterator<Item = proc_macro2::TokenTree>,
	) -> Vec<proc_macro2::TokenTree> {
		let mut result = Vec::new();
		for tt in tokens {
			match &tt {
				proc_macro2::TokenTree::Group(group) => {
					result.push(tt.clone());
					result.extend(recurse(group.stream()));
				}
				_ => result.push(tt),
			}
		}
		result
	}

	func.block
		.stmts
		.iter()
		.flat_map(|stmt| {
			let tokens: TokenStream = quote! { #stmt };
			recurse(tokens)
		})
		.collect()
}

/// Extract endpoint path expressions from a function body.
///
/// Scans tokens for `.endpoint(EXPR)` patterns and returns
/// the path expressions.
fn extract_endpoint_paths(func: &ItemFn) -> Vec<TokenStream> {
	let mut paths = Vec::new();
	let body_tokens = flatten_body(func);

	let mut i = 0;
	while i < body_tokens.len() {
		// Look for: Punct('.') Ident("endpoint") Group(Parenthesized)
		if i + 2 < body_tokens.len()
			&& let proc_macro2::TokenTree::Punct(p) = &body_tokens[i]
			&& p.as_char() == '.'
			&& let proc_macro2::TokenTree::Ident(ident) = &body_tokens[i + 1]
			&& ident == "endpoint"
			&& let proc_macro2::TokenTree::Group(group) = &body_tokens[i + 2]
			&& group.delimiter() == proc_macro2::Delimiter::Parenthesis
		{
			paths.push(group.stream());
			i += 3;
			continue;
		}
		i += 1;
	}
	paths
}

/// A viewset call extracted from the function body.
///
/// Example: `.viewset("/snippets", views::viewset())`
/// yields `ViewSetCall { expr_path: "views :: viewset" }`.
/// The `#[url_patterns]` macro uses `expr_path` to locate the
/// `__viewset_resolvers_{fn_name}` bundle module generated by `#[viewset]`.
struct ViewSetCall {
	expr_path: TokenStream,
}

/// Extract `.viewset(STR, EXPR)` calls from a function body.
///
/// Scans for the pattern `.viewset(string_literal, path_expr(...))` and
/// returns the second argument's path (stripping the trailing `()` call).
fn extract_viewset_calls(func: &ItemFn) -> Vec<ViewSetCall> {
	let mut calls = Vec::new();
	let body_tokens = flatten_body(func);

	let mut i = 0;
	while i < body_tokens.len() {
		if i + 2 < body_tokens.len()
			&& let proc_macro2::TokenTree::Punct(p) = &body_tokens[i]
			&& p.as_char() == '.'
			&& let proc_macro2::TokenTree::Ident(ident) = &body_tokens[i + 1]
			&& ident == "viewset"
			&& let proc_macro2::TokenTree::Group(group) = &body_tokens[i + 2]
			&& group.delimiter() == proc_macro2::Delimiter::Parenthesis
		{
			// Group contains: STR_LITERAL, EXPR (possibly with trailing `()`)
			let mut found_comma = false;
			let mut expr_tokens = Vec::new();
			for tt in group.stream() {
				if found_comma {
					// Skip trailing parenthesized group (the `()` function call)
					if let proc_macro2::TokenTree::Group(g) = &tt
						&& g.delimiter() == proc_macro2::Delimiter::Parenthesis
					{
						continue;
					}
					expr_tokens.push(tt);
				} else if let proc_macro2::TokenTree::Punct(p) = &tt
					&& p.as_char() == ','
				{
					found_comma = true;
				}
			}
			if !expr_tokens.is_empty() {
				let expr_path: TokenStream = expr_tokens.into_iter().collect();
				calls.push(ViewSetCall { expr_path });
			}
			i += 3;
			continue;
		}
		i += 1;
	}
	calls
}

/// A mount call extracted from the function body.
struct MountCall {
	expr_path: TokenStream,
}

/// Extract `.mount(STR, EXPR)` calls from a function body.
///
/// Scans for the pattern `.mount(string_literal, path_expr(...))` and
/// returns the second argument's path (stripping the trailing `()` call).
fn extract_mount_calls(func: &ItemFn) -> Vec<MountCall> {
	let mut calls = Vec::new();
	let body_tokens = flatten_body(func);

	let mut i = 0;
	while i < body_tokens.len() {
		if i + 2 < body_tokens.len()
			&& let proc_macro2::TokenTree::Punct(p) = &body_tokens[i]
			&& p.as_char() == '.'
			&& let proc_macro2::TokenTree::Ident(ident) = &body_tokens[i + 1]
			&& ident == "mount"
			&& let proc_macro2::TokenTree::Group(group) = &body_tokens[i + 2]
			&& group.delimiter() == proc_macro2::Delimiter::Parenthesis
		{
			let mut found_comma = false;
			let mut expr_tokens = Vec::new();
			for tt in group.stream() {
				if found_comma {
					if let proc_macro2::TokenTree::Group(g) = &tt
						&& g.delimiter() == proc_macro2::Delimiter::Parenthesis
					{
						continue;
					}
					expr_tokens.push(tt);
				} else if let proc_macro2::TokenTree::Punct(p) = &tt
					&& p.as_char() == ','
				{
					found_comma = true;
				}
			}
			if !expr_tokens.is_empty() {
				let expr_path: TokenStream = expr_tokens.into_iter().collect();
				calls.push(MountCall { expr_path });
			}
			i += 3;
			continue;
		}
		i += 1;
	}
	calls
}

/// Build a re-export statement for a URL resolver module from an endpoint path.
///
/// Given an endpoint path like `views::login`, generates:
/// `pub use super::views::__url_resolver_login::*;`
///
/// For absolute paths starting with `crate::` or `super::`, the `super::` prefix is omitted:
/// `pub use crate::views::__url_resolver_login::*;`
fn build_resolver_reexport(path: &TokenStream) -> TokenStream {
	let parsed: syn::Path = match syn::parse2(path.clone()) {
		Ok(p) => p,
		Err(_) => return quote! {},
	};

	if parsed.segments.is_empty() {
		return quote! {};
	}

	let last_segment = &parsed.segments.last().unwrap().ident;
	let resolver_mod = syn::Ident::new(
		&format!("__url_resolver_{last_segment}"),
		last_segment.span(),
	);

	let first_segment = parsed.segments.first().unwrap().ident.to_string();
	let is_absolute =
		first_segment == "crate" || first_segment == "super" || parsed.leading_colon.is_some();

	let parent_segments: Vec<&syn::Ident> = parsed
		.segments
		.iter()
		.take(parsed.segments.len() - 1)
		.map(|s| &s.ident)
		.collect();

	if is_absolute {
		quote! {
			pub use #(#parent_segments ::)* #resolver_mod::*;
		}
	} else {
		quote! {
			pub use super:: #(#parent_segments ::)* #resolver_mod::*;
		}
	}
}

/// Extract the metadata macro identifier from an endpoint path.
///
/// Given `views::login`, returns the identifier `__url_resolver_meta_login`.
/// The caller is responsible for qualifying the path (via `$base` in the
/// `__for_each_url_resolver` macro).
fn build_meta_ident(path: &TokenStream) -> Option<syn::Ident> {
	let parsed: syn::Path = syn::parse2(path.clone()).ok()?;
	let last_segment = &parsed.segments.last()?.ident;
	Some(syn::Ident::new(
		&format!("__url_resolver_meta_{last_segment}"),
		last_segment.span(),
	))
}

/// Build a re-export statement for a viewset's URL resolver bundle module.
///
/// Given a viewset call path like `views::viewset` (already stripped of
/// trailing `()`), extracts the function name (`viewset`) to reference the
/// `__viewset_resolvers_viewset` bundle module generated by `#[viewset]`.
///
/// The bundle module lives as a sibling of the annotated free function,
/// so only the function name (last segment) is stripped to get the module path.
fn build_viewset_reexport(call: &ViewSetCall) -> TokenStream {
	let parsed: syn::Path = match syn::parse2(call.expr_path.clone()) {
		Ok(p) => p,
		Err(_) => return quote! {},
	};

	if parsed.segments.is_empty() {
		return quote! {};
	}

	let last_segment = &parsed.segments.last().unwrap().ident;
	let bundle_mod = syn::Ident::new(
		&format!("__viewset_resolvers_{last_segment}"),
		last_segment.span(),
	);

	// Strip the function name to get the module path.
	// For `views::viewset`, module = `views`.
	let module_segments: Vec<&syn::Ident> = parsed
		.segments
		.iter()
		.take(parsed.segments.len() - 1)
		.map(|s| &s.ident)
		.collect();

	let first_segment = parsed.segments.first().unwrap().ident.to_string();
	let is_absolute = first_segment == "crate" || first_segment == "super";

	if is_absolute {
		if module_segments.is_empty() {
			quote! { pub use #bundle_mod::*; }
		} else {
			quote! { pub use #(#module_segments ::)* #bundle_mod::*; }
		}
	} else if module_segments.is_empty() {
		quote! { pub use super::#bundle_mod::*; }
	} else {
		quote! { pub use super:: #(#module_segments ::)* #bundle_mod::*; }
	}
}

/// Build a re-export statement for a mounted child router's `url_resolvers` module.
///
/// Given a mount path like `crate::apps::api::urls::url_patterns` (already
/// stripped of trailing `()`), strips the function name and re-exports
/// `url_resolvers::*` from the parent module.
fn build_mount_reexport(call: &MountCall) -> TokenStream {
	let parsed: syn::Path = match syn::parse2(call.expr_path.clone()) {
		Ok(p) => p,
		Err(_) => return quote! {},
	};

	if parsed.segments.is_empty() {
		return quote! {};
	}

	// Strip the last segment (the function name, e.g., `url_patterns`)
	let module_segments: Vec<&syn::Ident> = parsed
		.segments
		.iter()
		.take(parsed.segments.len() - 1)
		.map(|s| &s.ident)
		.collect();

	if module_segments.is_empty() {
		return quote! {};
	}

	let first_segment = parsed.segments.first().unwrap().ident.to_string();
	let is_absolute = first_segment == "crate" || first_segment == "super";

	if is_absolute {
		quote! {
			pub use #(#module_segments ::)* url_resolvers::*;
		}
	} else {
		quote! {
			pub use super:: #(#module_segments ::)* url_resolvers::*;
		}
	}
}

/// A named client route extracted from the function body.
///
/// Example: `.named_route("login_page", "/login/", handler)`
/// yields `ClientNamedRoute { name: "login_page", pattern: "/login/" }`.
struct ClientNamedRoute {
	name: String,
	pattern: String,
}

/// Extract `.named_route(NAME, PATTERN, HANDLER)` calls from a function body.
///
/// Scans for the pattern `.named_route("name", "/pattern/", expr)` and
/// extracts the route name and URL pattern strings.
fn extract_named_route_calls(func: &ItemFn) -> Vec<ClientNamedRoute> {
	let mut routes = Vec::new();
	let body_tokens = flatten_body(func);

	let mut i = 0;
	while i < body_tokens.len() {
		if i + 2 < body_tokens.len()
			&& let proc_macro2::TokenTree::Punct(p) = &body_tokens[i]
			&& p.as_char() == '.'
			&& let proc_macro2::TokenTree::Ident(ident) = &body_tokens[i + 1]
			&& (ident == "named_route"
				|| ident == "named_route_path"
				|| ident == "named_route_path2"
				|| ident == "named_route_path3"
				|| ident == "named_route_params"
				|| ident == "named_route_result")
			&& let proc_macro2::TokenTree::Group(group) = &body_tokens[i + 2]
			&& group.delimiter() == proc_macro2::Delimiter::Parenthesis
		{
			// Parse the first two string literal arguments: name, pattern
			let inner_tokens: Vec<proc_macro2::TokenTree> = group.stream().into_iter().collect();
			if let Some(route) = parse_named_route_args(&inner_tokens) {
				routes.push(route);
			}
			i += 3;
			continue;
		}
		i += 1;
	}
	routes
}

/// Parse the arguments of a `named_route(name, pattern, ...)` call.
///
/// Extracts the first two string literals (name and pattern).
fn parse_named_route_args(tokens: &[proc_macro2::TokenTree]) -> Option<ClientNamedRoute> {
	let mut literals = Vec::new();
	for tt in tokens {
		if let proc_macro2::TokenTree::Literal(lit) = tt {
			let lit_str = lit.to_string();
			// Check if it's a string literal (starts and ends with ")
			if lit_str.starts_with('"') && lit_str.ends_with('"') && lit_str.len() >= 2 {
				literals.push(lit_str[1..lit_str.len() - 1].to_string());
				if literals.len() == 2 {
					break;
				}
			}
		}
	}

	if literals.len() >= 2 {
		Some(ClientNamedRoute {
			name: literals[0].clone(),
			pattern: literals[1].clone(),
		})
	} else {
		None
	}
}

/// Extract URL parameter names from a pattern string.
///
/// Given `/users/{id}/posts/{post_id}/`, returns `["id", "post_id"]`.
fn extract_url_params(pattern: &str) -> Vec<String> {
	let mut params = Vec::new();
	let mut in_param = false;
	let mut name = String::new();
	for ch in pattern.chars() {
		match ch {
			'{' => {
				in_param = true;
				name.clear();
			}
			'}' => {
				if in_param && !name.is_empty() {
					// Strip type constraint (e.g., "{id:int}" → "id")
					if let Some(pos) = name.find(':') {
						name.truncate(pos);
					}
					params.push(name.clone());
				}
				in_param = false;
			}
			_ if in_param => name.push(ch),
			_ => {}
		}
	}
	params
}

/// Parsed arguments for `#[url_patterns]`.
struct UrlPatternsArgs {
	/// App label for namespacing (e.g., `"auth"`).
	app_label: Option<syn::LitStr>,
	/// Whether this is a client-side URL patterns function.
	client: bool,
}

/// Parse `#[url_patterns]` arguments.
///
/// Supports:
/// - `#[url_patterns]` — no args
/// - `#[url_patterns("auth")]` — app label only (backward compatible)
/// - `#[url_patterns(client = true, app = "auth")]` — client mode with app label
fn parse_url_patterns_args(args: TokenStream) -> syn::Result<UrlPatternsArgs> {
	if args.is_empty() {
		return Ok(UrlPatternsArgs {
			app_label: None,
			client: false,
		});
	}

	// Try parsing as a single string literal first (backward compatible)
	if let Ok(lit) = syn::parse2::<syn::LitStr>(args.clone()) {
		return Ok(UrlPatternsArgs {
			app_label: Some(lit),
			client: false,
		});
	}

	// Parse as key-value pairs: client = true, app = "auth"
	let meta_list: syn::punctuated::Punctuated<syn::Meta, syn::Token![,]> =
		syn::parse::Parser::parse2(
			syn::punctuated::Punctuated::<syn::Meta, syn::Token![,]>::parse_terminated,
			args,
		)?;

	let mut client = false;
	let mut app_label = None;

	for meta in &meta_list {
		match meta {
			syn::Meta::NameValue(nv) if nv.path.is_ident("client") => {
				if let syn::Expr::Lit(syn::ExprLit {
					lit: syn::Lit::Bool(b),
					..
				}) = &nv.value
				{
					client = b.value;
				} else {
					return Err(syn::Error::new_spanned(
						&nv.value,
						"expected boolean value for `client`",
					));
				}
			}
			syn::Meta::NameValue(nv) if nv.path.is_ident("app") => {
				if let syn::Expr::Lit(syn::ExprLit {
					lit: syn::Lit::Str(s),
					..
				}) = &nv.value
				{
					app_label = Some(s.clone());
				} else {
					return Err(syn::Error::new_spanned(
						&nv.value,
						"expected string literal for `app`",
					));
				}
			}
			_ => {
				return Err(syn::Error::new_spanned(
					meta,
					"unknown attribute, expected `client = true` or `app = \"name\"`",
				));
			}
		}
	}

	if client && app_label.is_none() {
		return Err(syn::Error::new(
			proc_macro2::Span::call_site(),
			"`app` is required when `client = true`",
		));
	}

	Ok(UrlPatternsArgs { app_label, client })
}

/// Implementation of the `#[url_patterns]` attribute macro.
///
/// Supports two modes:
///
/// **Server mode** (default):
/// - `#[url_patterns]` or `#[url_patterns("app_label")]`
/// - Scans for `.endpoint()`, `.viewset()`, `.mount()` calls
/// - Generates `url_resolvers` module with metadata macros
///
/// **Client mode:**
/// - `#[url_patterns(client = true, app = "app_label")]`
/// - Scans for `.named_route()` calls
/// - Generates `client_url_resolvers` module with metadata macros
/// - Prefixes route names with `"app:"` at runtime
pub(crate) fn url_patterns_impl(args: TokenStream, input: TokenStream) -> syn::Result<TokenStream> {
	let parsed_args = parse_url_patterns_args(args)?;

	if parsed_args.client {
		return url_patterns_client_impl(parsed_args, input);
	}

	url_patterns_server_impl(parsed_args, input)
}

/// Server-mode implementation (original behavior).
fn url_patterns_server_impl(
	parsed_args: UrlPatternsArgs,
	input: TokenStream,
) -> syn::Result<TokenStream> {
	let func: ItemFn = parse2(input)?;

	let endpoint_paths = extract_endpoint_paths(&func);
	let viewset_calls = extract_viewset_calls(&func);
	let mount_calls = extract_mount_calls(&func);

	let endpoint_re_exports = endpoint_paths.iter().map(build_resolver_reexport);
	let viewset_re_exports = viewset_calls.iter().map(build_viewset_reexport);
	let mount_re_exports = mount_calls.iter().map(build_mount_reexport);
	let meta_idents: Vec<syn::Ident> = endpoint_paths.iter().filter_map(build_meta_ident).collect();

	let func_output = if let Some(app_label) = &parsed_args.app_label {
		let fn_vis = &func.vis;
		let fn_attrs = &func.attrs;
		let fn_sig = &func.sig;
		let fn_block = &func.block;

		quote! {
			#(#fn_attrs)*
			#fn_vis #fn_sig {
				let __router = (|| #fn_block)();
				__router.with_namespace(#app_label)
			}
		}
	} else {
		quote! { #func }
	};

	Ok(quote! {
		#func_output

		#[doc(hidden)]
		pub mod url_resolvers {
			#(
				#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
				#endpoint_re_exports
			)*
			#(
				#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
				#viewset_re_exports
			)*
			#(
				#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
				#mount_re_exports
			)*

			/// Invoke a callback macro for each URL resolver in this app.
			/// Used by the `#[routes]` macro to build per-app resolver structs.
			/// `$base` must be the absolute path to this `url_resolvers` module
			/// so that the metadata macros resolve at the call site.
			#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
			macro_rules! __for_each_url_resolver {
				($callback:ident, $app:ident, $($base:tt)+) => {
					#(
						$($base)+ :: #meta_idents ! ($callback, $app);
					)*
				};
			}
			#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
			pub(crate) use __for_each_url_resolver;
		}
	})
}

/// Client-mode implementation for `#[url_patterns(client = true, app = "...")]`.
///
/// Scans the function body for `.named_route()` calls and generates:
/// 1. Runtime code that prefixes route names with `"app:"`
/// 2. `client_url_resolvers` module with per-route metadata macros
fn url_patterns_client_impl(
	parsed_args: UrlPatternsArgs,
	input: TokenStream,
) -> syn::Result<TokenStream> {
	let func: ItemFn = parse2(input)?;
	let app_label = parsed_args
		.app_label
		.as_ref()
		.expect("app_label required for client mode");
	let app_str = app_label.value();

	let named_routes = extract_named_route_calls(&func);

	// Generate per-route metadata macros
	let mut meta_macro_defs = Vec::new();
	let mut meta_idents = Vec::new();

	for route in &named_routes {
		// Validate route name is a valid Rust identifier
		if syn::parse_str::<syn::Ident>(&route.name).is_err() {
			return Err(syn::Error::new(
				proc_macro2::Span::call_site(),
				format!(
					"Client route name `{}` is not a valid Rust identifier. \
					 Route names must be valid identifiers (no hyphens, dots, or leading digits).",
					route.name
				),
			));
		}

		let method_ident = syn::Ident::new(&route.name, proc_macro2::Span::call_site());
		let route_name_str = &route.name;
		let meta_macro_ident = syn::Ident::new(
			&format!("__client_url_resolver_meta_{}", route.name),
			proc_macro2::Span::call_site(),
		);

		let params = extract_url_params(&route.pattern);
		let param_strs: Vec<&str> = params.iter().map(|s| s.as_str()).collect();

		let meta_def = if params.is_empty() {
			quote! {
				#[doc(hidden)]
				macro_rules! #meta_macro_ident {
					($callback:ident, $app:ident) => {
						$callback!($app, #method_ident, #route_name_str, );
					};
				}
				pub(crate) use #meta_macro_ident;
			}
		} else {
			quote! {
				#[doc(hidden)]
				macro_rules! #meta_macro_ident {
					($callback:ident, $app:ident) => {
						$callback!($app, #method_ident, #route_name_str, #(#param_strs),* );
					};
				}
				pub(crate) use #meta_macro_ident;
			}
		};

		meta_macro_defs.push(meta_def);
		meta_idents.push(meta_macro_ident);
	}

	// Wrap the function to prefix route names with "app:"
	let fn_vis = &func.vis;
	let fn_attrs = &func.attrs;
	let fn_sig = &func.sig;
	let fn_block = &func.block;

	// Wrap the function body to apply `with_namespace("app")` to the
	// returned `ClientRouter`. This prefixes all named route keys with
	// "app:" so that `to_reverser()` produces names matching the
	// "app:route" format used by per-app resolver structs.
	let func_output = quote! {
		#(#fn_attrs)*
		#fn_vis #fn_sig {
			let __router = (|| #fn_block)();
			__router.with_namespace(#app_str)
		}
	};

	Ok(quote! {
		#func_output

		#[doc(hidden)]
		pub mod client_url_resolvers {
			#(
				#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
				#meta_macro_defs
			)*

			/// Invoke a callback macro for each client URL resolver in this app.
			/// Used by the `#[routes]` macro to build per-app client resolver structs.
			/// `$base` must be the absolute path to this `client_url_resolvers` module
			/// so that the metadata macros resolve at the call site.
			#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
			macro_rules! __for_each_client_url_resolver {
				($callback:ident, $app:ident, $base:path) => {
					#(
						$base :: #meta_idents ! ($callback, $app);
					)*
				};
			}
			#[cfg(not(all(target_family = "wasm", target_os = "unknown")))]
			pub(crate) use __for_each_client_url_resolver;
		}
	})
}

